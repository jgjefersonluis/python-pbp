# Inteiros
a = 28
print(a)
# Saída: 28

# Ponto flutuante
pi = 3.1415
print(pi)
# Saída: 3.14.15

# String
name = 'Alexsandro Felix'
print(name)
# Saída: Alexsandro Felix

# Boolean
b = True
print(b)
# Saída: True

# Valor vazio ou dado do tipo nulo
x = None
print(x)
# Saída: None

# Variável declarada de forma errada
# 0 = x
# Saída: SyntaxError: can't assign to literal